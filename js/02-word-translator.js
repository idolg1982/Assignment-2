var language = prompt('Enter a language that you would like "Hello world" translated into, ("es", "de", "en", and "fr")');

if (language == "fr"){
    alert("Hello World translated in French is: Bonjour le monde");
}

else if (language == "de"){
    alert("Hello World translated in German is: Hallo Welt");
}

else if (language == "es"){
    alert("Hello World translated in Spanish is: Hola mundo");
}

else if (language == "en"){
    alert("Hello World in English is: Hello world");
}

else{
    alert("Hello World in English is: Hello world");
}